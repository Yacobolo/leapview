func TestGenerateAPIGenObjectScopes(t *testing.T) {
	ir := []byte(`{
		"endpoints": [
			{
				"operation_id": "queryDashboard",
				"path": "/api/v1/workspaces/{workspace}/dashboards/{dashboard}/query",
				"extensions": {"x-libredash-object-scope": "dashboard"}
			},
			{
				"operation_id": "getAsset",
				"path": "/api/v1/workspaces/{workspace}/assets/{assetId}",
				"extensions": {"x-libredash-object-scope": "workspace-asset"}
			},
			{
				"operation_id": "listAssets",
				"path": "/api/v1/workspaces/{workspace}/assets",
				"extensions": {}
			}
		]
	}`)

	generated, err := generateAPIGenObjectScopes(ir)
	if err != nil {
		t.Fatalf("generate object scopes: %v", err)
	}
	text := string(generated)
	normalized := strings.Join(strings.Fields(text), " ")
	for _, want := range []string{
		"// Code generated by internal/tools/apigenpostprocess; DO NOT EDIT.",
		"type apiGenObjectScope string",
		`apiGenObjectScopeDashboard apiGenObjectScope = "dashboard"`,
		`apiGenObjectScopeWorkspaceAsset apiGenObjectScope = "workspace-asset"`,
		`"getAsset": apiGenObjectScopeWorkspaceAsset`,
		`"queryDashboard": apiGenObjectScopeDashboard`,
	} {
		if !strings.Contains(normalized, want) {
			t.Errorf("generated object scopes missing %q:\n%s", want, text)
		}
	}
	if strings.Contains(text, "listAssets") {
		t.Fatalf("workspace-scoped operation received an exact-object scope:\n%s", text)
	}
	if strings.Index(text, `"getAsset"`) > strings.Index(text, `"queryDashboard"`) {
		t.Fatalf("generated operations are not sorted:\n%s", text)
	}
}

func TestGenerateAPIGenObjectScopesRejectsInvalidContracts(t *testing.T) {
	tests := []struct {
		name      string
		endpoints string
		want      string
	}{
		{name: "invalid document", endpoints: `not-json`, want: "decode APIGen IR"},
		{name: "no endpoints", endpoints: `[]`, want: "contains no endpoints"},
		{
			name:      "missing operation id",
			endpoints: `[ {"path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "missing operation_id",
		},
		{
			name:      "duplicate operation id",
			endpoints: `[ {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "dashboard"}}, {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "duplicate operation_id",
		},
		{
			name:      "missing path",
			endpoints: `[ {"operation_id": "getDashboard", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "getDashboard: missing path",
		},
		{
			name:      "path with surrounding whitespace",
			endpoints: `[ {"operation_id": "getDashboard", "path": " /dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "getDashboard: path contains surrounding whitespace",
		},
		{
			name:      "missing scope metadata",
			endpoints: `[ {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {}} ]`,
			want:      "getDashboard: missing x-libredash-object-scope",
		},
		{
			name:      "malformed scope metadata",
			endpoints: `[ {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": {"kind": "dashboard"}}} ]`,
			want:      "getDashboard: decode x-libredash-object-scope",
		},
		{
			name:      "wrong scope",
			endpoints: `[ {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "semantic-model"}} ]`,
			want:      `getDashboard: object scope "semantic-model" does not match path scope "dashboard"`,
		},
		{
			name:      "unknown scope",
			endpoints: `[ {"operation_id": "getDashboard", "path": "/dashboards/{dashboard}", "extensions": {"x-libredash-object-scope": "tenant"}} ]`,
			want:      `getDashboard: unknown object scope "tenant"`,
		},
		{
			name:      "unexpected scope",
			endpoints: `[ {"operation_id": "listDashboards", "path": "/dashboards", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "listDashboards: object scope metadata requires a supported exact-object path parameter",
		},
		{
			name:      "ambiguous path",
			endpoints: `[ {"operation_id": "getThing", "path": "/dashboards/{dashboard}/models/{model}", "extensions": {"x-libredash-object-scope": "dashboard"}} ]`,
			want:      "getThing: path selects multiple object scopes",
		},
	}

	for _, test := range tests {
		t.Run(test.name, func(t *testing.T) {
			_, err := generateAPIGenObjectScopes([]byte(`{"endpoints":` + test.endpoints + `}`))
			if err == nil || !strings.Contains(err.Error(), test.want) {
				t.Fatalf("error = %v, want containing %q", err, test.want)
			}
		})
	}
}
