package main

import (
	"encoding/json"
	"fmt"
	"go/format"
	"os"
	"sort"
	"strings"
)

const apiGenObjectScopeExtension = "x-libredash-object-scope"

type apiGenOperationObjectScope struct {
	OperationID string
	Scope       string
}

type apiGenObjectScopeDefinition struct {
	Value      string
	Identifier string
	PathParam  string
}

var apiGenObjectScopeDefinitions = []apiGenObjectScopeDefinition{
	{Value: "agent-conversation", Identifier: "AgentConversation", PathParam: "conversation"},
	{Value: "dashboard", Identifier: "Dashboard", PathParam: "dashboard"},
	{Value: "semantic-model", Identifier: "SemanticModel", PathParam: "model"},
	{Value: "workspace-asset", Identifier: "WorkspaceAsset", PathParam: "assetId"},
}

func writeAPIGenObjectScopes(irPath, outputPath string) error {
	ir, err := os.ReadFile(irPath)
	if err != nil {
		return fmt.Errorf("read APIGen IR for object scopes: %w", err)
	}
	generated, err := generateAPIGenObjectScopes(ir)
	if err != nil {
		return err
	}
	if err := os.WriteFile(outputPath, generated, 0o644); err != nil {
		return fmt.Errorf("write APIGen object scopes: %w", err)
	}
	return nil
}

func generateAPIGenObjectScopes(ir []byte) ([]byte, error) {
	document, err := decodeAPIGenIR(ir)
	if err != nil {
		return nil, err
	}
	endpoints, err := validateAPIGenEndpoints(document.Endpoints)
	if err != nil {
		return nil, err
	}

	definitions := make(map[string]apiGenObjectScopeDefinition, len(apiGenObjectScopeDefinitions))
	for _, definition := range apiGenObjectScopeDefinitions {
		definitions[definition.Value] = definition
	}
	operations := make([]apiGenOperationObjectScope, 0, len(endpoints))
	for _, endpoint := range endpoints {
		path := strings.TrimSpace(endpoint.Path)
		if path == "" {
			return nil, fmt.Errorf("%s: missing path", endpoint.OperationID)
		}
		if path != endpoint.Path {
			return nil, fmt.Errorf("%s: path contains surrounding whitespace", endpoint.OperationID)
		}
		expected, ambiguous := apiGenObjectScopeForPath(path)
		if ambiguous {
			return nil, fmt.Errorf("%s: path selects multiple object scopes", endpoint.OperationID)
		}
		rawScope, hasScope := endpoint.Extensions[apiGenObjectScopeExtension]
		if !hasScope {
			if expected != "" {
				return nil, fmt.Errorf("%s: missing %s for path scope %q", endpoint.OperationID, apiGenObjectScopeExtension, expected)
			}
			continue
		}
		var scope string
		if err := json.Unmarshal(rawScope, &scope); err != nil {
			return nil, fmt.Errorf("%s: decode %s: %w", endpoint.OperationID, apiGenObjectScopeExtension, err)
		}
		definition, known := definitions[scope]
		if !known {
			return nil, fmt.Errorf("%s: unknown object scope %q", endpoint.OperationID, scope)
		}
		if expected == "" {
			return nil, fmt.Errorf("%s: object scope metadata requires a supported exact-object path parameter", endpoint.OperationID)
		}
		if scope != expected {
			return nil, fmt.Errorf("%s: object scope %q does not match path scope %q", endpoint.OperationID, scope, expected)
		}
		operations = append(operations, apiGenOperationObjectScope{OperationID: endpoint.OperationID, Scope: definition.Value})
	}
	sort.Slice(operations, func(i, j int) bool {
		return operations[i].OperationID < operations[j].OperationID
	})

	var output strings.Builder
	output.WriteString("// Code generated by internal/tools/apigenpostprocess; DO NOT EDIT.\n\n")
	output.WriteString("package app\n\n")
	output.WriteString("type apiGenObjectScope string\n\n")
	output.WriteString("const (\n")
	for _, definition := range apiGenObjectScopeDefinitions {
		fmt.Fprintf(&output, "\tapiGenObjectScope%s apiGenObjectScope = %q\n", definition.Identifier, definition.Value)
	}
	output.WriteString(")\n\n")
	output.WriteString("var apigenOperationObjectScopes = map[string]apiGenObjectScope{\n")
	for _, operation := range operations {
		definition := definitions[operation.Scope]
		fmt.Fprintf(&output, "\t%q: apiGenObjectScope%s,\n", operation.OperationID, definition.Identifier)
	}
	output.WriteString("}\n")

	formatted, err := format.Source([]byte(output.String()))
	if err != nil {
		return nil, fmt.Errorf("format APIGen object scopes: %w", err)
	}
	return formatted, nil
}

func apiGenObjectScopeForPath(path string) (string, bool) {
	matched := ""
	for _, definition := range apiGenObjectScopeDefinitions {
		if !strings.Contains(path, "{"+definition.PathParam+"}") {
			continue
		}
		if matched != "" {
			return "", true
		}
		matched = definition.Value
	}
	return matched, false
}
