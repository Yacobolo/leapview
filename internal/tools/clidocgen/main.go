// Command clidocgen renders the Cobra command tree into public Markdown docs.
package main

import (
	"context"
	"encoding/json"
	"flag"
	"fmt"
	"os"
	"path/filepath"
	"sort"
	"strings"

	"github.com/Yacobolo/libredash/internal/cli"
	"github.com/spf13/cobra"
	"github.com/spf13/pflag"
)

type catalog struct {
	SchemaVersion int        `json:"schemaVersion"`
	Title         string     `json:"title"`
	Documents     []document `json:"documents"`
}
type document struct {
	Slug    string `json:"slug"`
	Title   string `json:"title"`
	Summary string `json:"summary"`
}

const (
	effectAnnotation       = "libredash.dev/effect"
	confirmationAnnotation = "libredash.dev/confirmation"
)

type machineManifest struct {
	SchemaVersion int              `json:"schemaVersion"`
	Commands      []machineCommand `json:"commands"`
}

type machineCommand struct {
	ID               string          `json:"id"`
	Path             []string        `json:"path"`
	Title            string          `json:"title"`
	Summary          string          `json:"summary"`
	Description      string          `json:"description"`
	Usage            string          `json:"usage"`
	Runnable         bool            `json:"runnable"`
	Effect           string          `json:"effect"`
	Confirmation     string          `json:"confirmation"`
	Arguments        []string        `json:"arguments"`
	Options          []machineOption `json:"options"`
	InheritedOptions []machineOption `json:"inheritedOptions"`
	Examples         []string        `json:"examples"`
	Subcommands      []string        `json:"subcommands"`
}

type machineOption struct {
	Name        string `json:"name"`
	Shorthand   string `json:"shorthand,omitempty"`
	Type        string `json:"type"`
	Default     string `json:"default"`
	Description string `json:"description"`
	Required    bool   `json:"required"`
}

func main() {
	out := flag.String("out", "docs/reference/cli", "generated Markdown output directory")
	flag.Parse()
	if err := generate(cli.NewCommand(context.Background()), *out); err != nil {
		fmt.Fprintln(os.Stderr, err)
		os.Exit(1)
	}
}

func generate(root *cobra.Command, out string) error {
	if err := os.RemoveAll(out); err != nil {
		return err
	}
	if err := os.MkdirAll(out, 0o755); err != nil {
		return err
	}
	items := []document{}
	commands := []machineCommand{}
	var visit func(*cobra.Command, []string) error
	visit = func(command *cobra.Command, path []string) error {
		if command.Hidden {
			return nil
		}
		if command != root {
			slug := strings.Join(path, "-")
			item := document{Slug: slug, Title: "libredash " + strings.Join(path, " "), Summary: command.Short}
			machine, err := machineCommandFrom(command, path, item)
			if err != nil {
				return err
			}
			items = append(items, item)
			commands = append(commands, machine)
			if err := os.WriteFile(filepath.Join(out, slug+".md"), []byte(render(command, item)), 0o644); err != nil {
				return err
			}
		}
		children := command.Commands()
		sort.Slice(children, func(i, j int) bool { return children[i].Name() < children[j].Name() })
		for _, child := range children {
			if err := visit(child, append(path, child.Name())); err != nil {
				return err
			}
		}
		return nil
	}
	if err := visit(root, nil); err != nil {
		return err
	}
	sort.Slice(items, func(i, j int) bool { return items[i].Title < items[j].Title })
	encoded, err := json.MarshalIndent(catalog{SchemaVersion: 1, Title: "CLI reference", Documents: items}, "", "  ")
	if err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(out, "catalog.json"), append(encoded, '\n'), 0o644); err != nil {
		return err
	}
	machineEncoded, err := json.MarshalIndent(machineManifest{SchemaVersion: 1, Commands: commands}, "", "  ")
	if err != nil {
		return err
	}
	if err := os.WriteFile(filepath.Join(out, "manifest.json"), append(machineEncoded, '\n'), 0o644); err != nil {
		return err
	}
	var index strings.Builder
	index.WriteString("<!-- Code generated by internal/tools/clidocgen; DO NOT EDIT. -->\n\n# CLI reference\n\n")
	index.WriteString("The complete reference for the `libredash` command-line interface.\n\n## Commands\n\n")
	index.WriteString("For agents and tooling, use the [versioned CLI manifest](/docs/cli/manifest.json) or the read-only [documentation MCP](/mcp). Focused command slices are available at `/docs/cli/commands/{id}.json` and `.md`.\n\n")
	for _, item := range items {
		index.WriteString(fmt.Sprintf("- [%s](/docs/cli/%s): %s\n", item.Title, item.Slug, item.Summary))
	}
	return os.WriteFile(filepath.Join(out, "index.md"), []byte(index.String()), 0o644)
}

func machineCommandFrom(command *cobra.Command, path []string, item document) (machineCommand, error) {
	effect, confirmation := "none", "never"
	if command.Runnable() {
		effect = command.Annotations[effectAnnotation]
		if effect == "" {
			return machineCommand{}, fmt.Errorf("command %q is runnable but missing %s annotation", command.CommandPath(), effectAnnotation)
		}
		confirmation = command.Annotations[confirmationAnnotation]
		if confirmation == "" {
			confirmation = defaultConfirmation(effect)
		}
	}
	description := strings.TrimSpace(command.Long)
	if description == "" {
		description = command.Short
	}
	children := visibleChildren(command)
	subcommands := make([]string, 0, len(children))
	for _, child := range children {
		subcommands = append(subcommands, strings.Join(append(append([]string(nil), path...), child.Name()), "-"))
	}
	examples := []string{}
	for _, line := range strings.Split(strings.TrimSpace(command.Example), "\n") {
		if line = strings.TrimSpace(line); line != "" {
			examples = append(examples, line)
		}
	}
	return machineCommand{
		ID:               item.Slug,
		Path:             append([]string(nil), path...),
		Title:            item.Title,
		Summary:          item.Summary,
		Description:      description,
		Usage:            command.UseLine(),
		Runnable:         command.Runnable(),
		Effect:           effect,
		Confirmation:     confirmation,
		Arguments:        commandArguments(command.Use),
		Options:          machineOptions(command.LocalFlags()),
		InheritedOptions: machineOptions(command.InheritedFlags()),
		Examples:         examples,
		Subcommands:      subcommands,
	}, nil
}

func defaultConfirmation(effect string) string {
	switch effect {
	case "destructive":
		return "required"
	case "write", "idempotent-write":
		return "conditional"
	default:
		return "never"
	}
}

func commandArguments(use string) []string {
	fields := strings.Fields(use)
	arguments := []string{}
	for _, field := range fields[1:] {
		if (strings.HasPrefix(field, "<") && strings.HasSuffix(field, ">")) ||
			(strings.HasPrefix(field, "[") && strings.HasSuffix(field, "]")) {
			arguments = append(arguments, strings.Trim(field, "<>[]"))
		}
	}
	return arguments
}

func machineOptions(flags *pflag.FlagSet) []machineOption {
	options := []machineOption{}
	flags.VisitAll(func(flag *pflag.Flag) {
		options = append(options, machineOption{
			Name:        flag.Name,
			Shorthand:   flag.Shorthand,
			Type:        flag.Value.Type(),
			Default:     flag.DefValue,
			Description: flag.Usage,
			Required:    len(flag.Annotations[cobra.BashCompOneRequiredFlag]) > 0,
		})
	})
	return options
}

func render(command *cobra.Command, item document) string {
	var out strings.Builder
	out.WriteString("<!-- Code generated by internal/tools/clidocgen; DO NOT EDIT. -->\n\n# " + item.Title + "\n\n")
	if command.Long != "" {
		out.WriteString(strings.TrimSpace(command.Long))
	} else {
		out.WriteString(command.Short)
	}
	out.WriteString("\n\n## Usage\n\n```sh\n" + command.UseLine() + "\n```\n")
	if command.Runnable() {
		out.WriteString("\n## Behavior\n\n| Side effect | Confirmation |\n| --- | --- |\n")
		out.WriteString("| `" + command.Annotations[effectAnnotation] + "` | `" + command.Annotations[confirmationAnnotation] + "` |\n")
		out.WriteString("\nMachine-readable: [JSON](/docs/cli/commands/" + item.Slug + ".json) · [Markdown](/docs/cli/commands/" + item.Slug + ".md)\n")
	}
	writeFlags(&out, "Options", command.LocalFlags())
	if inherited := command.InheritedFlags(); inherited.HasFlags() {
		writeFlags(&out, "Inherited options", inherited)
	}
	if len(command.Example) > 0 {
		out.WriteString("\n## Examples\n\n```sh\n" + strings.TrimSpace(command.Example) + "\n```\n")
	}
	children := visibleChildren(command)
	if len(children) > 0 {
		out.WriteString("\n## Subcommands\n\n")
		for _, child := range children {
			out.WriteString(fmt.Sprintf("- `libredash %s`: %s\n", child.Name(), child.Short))
		}
	}
	return out.String()
}

func writeFlags(out *strings.Builder, heading string, flags *pflag.FlagSet) {
	if !flags.HasFlags() {
		return
	}
	out.WriteString("\n## " + heading + "\n\n| Flag | Type | Default | Description |\n| --- | --- | --- | --- |\n")
	flags.VisitAll(func(value *pflag.Flag) {
		name := "`--" + value.Name + "`"
		if value.Shorthand != "" {
			name = "`-" + value.Shorthand + "`, " + name
		}
		out.WriteString(fmt.Sprintf("| %s | %s | `%s` | %s |\n", name, value.Value.Type(), value.DefValue, value.Usage))
	})
}

func visibleChildren(command *cobra.Command) []*cobra.Command {
	children := []*cobra.Command{}
	for _, child := range command.Commands() {
		if !child.Hidden {
			children = append(children, child)
		}
	}
	return children
}
